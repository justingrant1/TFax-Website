// ─── Helpers ──────────────────────────────────────────────────────────────────

export function getCoverSheetBySlug(slug: string): CoverSheet | undefined {
  return coverSheets.find((s) => s.slug === slug)
}

export function getCoverSheetsByType(type: CoverSheet["type"]): CoverSheet[] {
  return coverSheets.filter((s) => s.type === type)
}

export function getSiblingCoverSheets(sheet: CoverSheet, count = 3): CoverSheet[] {
  return coverSheets
    .filter((s) => s.slug !== sheet.slug && s.type === sheet.type)
    .slice(0, count)
}
